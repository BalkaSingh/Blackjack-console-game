//Enum that represents the suits of cards 
public enum Suit{
	//toString method for each : returns the string that represents the suit
	HEART{
		public String toString(){
			return "HEART";
		}
	},
	SPADE{
		public String toString(){
			return "SPADE";
		}
	},
	CLUB{
		public String toString(){
			return "CLUB";
		}
	},
	DIAMOND{
		public String toString(){
			return "DIAMOND";
		}
	}
}